<blockquote><b><font color=#FF0000>CIRCLE C++. </font> 
El sistema C&iacute;rculo sirve para analizar sistemas 
din&aacute;micos discretos en la circunferencia.  Estos sistemas est&aacute;n gobernados 
por las iteraciones de una funci&oacute;n de la circunferencia en la 
circunferencia. Muchos fen&oacute;menos de inter&eacute;s en Ciencia e Ingenier&iacute;a pueden 
ser modelados por medio de sistemas din&aacute;micos en la circunferencia. En 
particular este es el caso de muchos problemas interesantes de la teor&iacute;a 
de oscilaciones no lineales. El sistema C&iacute;rculo ha sido desarrollado 
usando la metodolog&iacute;a orientada a objetos.<br>
El sistema permite analizar la din&aacute;mica de estos sistemas y analizar los 
distintos comportamientos que se pueden producir cuando se varian los 
valores de las condiciones iniciales y los par&aacute;metros. El sistema permite: 
un an&aacute;lisis visual basado en el c&aacute;lculo y despliegue de las graficas de 
los levantamientos de las funciones de la circunferencia y de las graficas 
de las funciones en el toro plano; el despliegue de &oacute;rbitas, c&aacute;lculo del 
n&uacute;mero de rotaci&oacute;n, c&aacute;lculo de los &iacute;ndices de los atractores peri&oacute;dicos 
(sincronizaciones) y c&aacute;lculo de los exponentes de Lyapounov entre otras 
funciones.<br>


